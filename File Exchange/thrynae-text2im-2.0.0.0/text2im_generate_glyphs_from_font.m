function IM=text2im_generate_glyphs_from_font(font)
%The list of included characters is based on a relatively arbitrary
%selection from the pages below.
%https://en.wikipedia.org/wiki/List_of_Unicode_characters
%https://en.wikipedia.org/wiki/Newline#Unicode
%https://en.wikipedia.org/wiki/Whitespace_character#Unicode
%
%Requirements to generate the text2im_glyph image:
%[NB: all are set as hardcoded paths and system specific names]
%- miktex portable (you need to have checked for updates at least once)
%- ghostscript
%- font installed as system font
%
%The ASCII 'font' is adapted from the FEX submission by Tobias Kiessling
%http://web.archive.org/web/20181129091314/
%https://www.mathworks.com/matlabcentral/mlc-downloads/downloads/
%submissions/19896/versions/1/contents/text2im.m

font_list={'CMU Typewriter Text','CMU Concrete','ASCII','Droid Sans Mono','IBM Plex Mono','Liberation Mono','Monoid'};
if nargin==0
    font=font_list{1};
elseif isa(font,'double')
    font=font_list{font};
else
    %font should be a valid font name
end
p_tmp='C:\tmp\';
p_miktex=[p_tmp 'miktex-portable\'];
xelatex=[p_miktex 'texmfs\install\miktex\bin\x64\xelatex.exe'];
ghostscript='"C:\Program Files\gs\gs9.52\bin\gswin64.exe"';

name_base=sprintf('glyphs_%s',font );
pdffullfile=[p_tmp name_base '.pdf'];
im_name=[p_tmp name_base '.png'];
texname=[p_tmp name_base '.tex'];
final_image=[p_tmp 'text2im_' strrep(name_base,' ','_') '.png'];

if exist(final_image,'file')
    if nargout>0
        IM=imread(final_image);
    end
    return
end

%Base list; characters that are missing from the font will be removed.
printable=sort([33:126 161:172 174:328 330:383 913:929 931:1023 ...
    8211:8213 8215:8222 8224:8226 8230 8240 8227 8243 8249 8250 8252 ...
    8254 8260 8266 8352:8383 8448:8527 8592:8703]);

if strcmpi(font,'ASCII')
    %extract the ASCII chars from the FEX submission by Tobias Kiessling
    %http://web.archive.org/web/20181129091314/
    %https://www.mathworks.com/matlabcentral/mlc-downloads/downloads/
    %submissions/19896/versions/1/contents/text2im.m
    list=printable(printable<=127);
    c=cell(numel(list),1);
    for n=1:numel(list)
        c{n}=~text2im_legacy(list(n));
    end
    
    IM=create_font_png(c,printable);
    imwrite(IM,final_image);
    return
end

if ~exist(im_name,'file')
    fid=fopen(texname,'w');
    header={'\documentclass[10pt,a4paper]{article}';...
        '\usepackage{xltxtra,fontspec,xunicode}';['\usepackage[paperheig',...
        'ht=10000bp,inner=50bp,outer=30bp,top=50bp,bottom=50bp]{geometry}'];...
        sprintf('\\setromanfont{%s}',font);'\begin{document}';...
        '\thispagestyle{empty}';'\begin{tabular}{|r|c|}\hline'};
    for n=1:numel(header)
        fprintf(fid,'%s\n',header{n});
    end
    for n=1:numel(printable)
        if printable(n)==185
            fprintf(fid,'(codepoint=%4d) & $^1$\\\\\\hline\n',...
                printable(n));
            continue
        elseif printable(n)==178
            fprintf(fid,'(codepoint=%4d) & $^2$\\\\\\hline\n',...
                printable(n));
            continue
        elseif printable(n)==179
            fprintf(fid,'(codepoint=%4d) & $^3$\\\\\\hline\n',...
                printable(n));
            continue
        end
        %this XeTeX code will only print a char if it has a glyph
        char_or_empty=sprintf('\\ifnum\\XeTeXcharglyph%d>0\\char%d\\fi',...
            printable(n),printable(n));
        fprintf(fid,'(codepoint=%4d) & %s\\\\\\hline\n',...
            printable(n),char_or_empty);
    end
    fprintf(fid,'%s\n%s','\end{tabular}','\end{document}');
    fclose(fid);
    
    %create temp batch file
    tmp_bat=[p_tmp 'tmp_bat.bat'];
    fid=fopen(tmp_bat,'w');
    fprintf(fid,'%s\n','cd /d %~dp0');%cd to the batch file location

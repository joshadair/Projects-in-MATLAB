%cut off empty top and bottom
m=min(IM,[],2);
IM(m,:)=[];

%get the column with the glyphs
m=all(~IM,1);
idx=find(m);
idx2=[diff(idx) 1];
idx2=find(idx2~=1);

cols=[idx(idx2(2))+1 idx(idx2(2)+1)-1];
glyphs=IM(:,cols(1):cols(2));

m=all(~glyphs,2)';%find table lines
idx=find(m);
idx2=[diff(idx) 1];
idx2=find(idx2~=1);

%font_list={'CMU Typewriter Text','CMU Concrete','ASCII','Droid Sans Mono','IBM Plex Mono','Liberation Mono','Monoid'};
switch font
    case 'CMU Typewriter Text'
        %for 10 point font and 600 dpi
        n_rows=90;
        glyph_width=51;%clips promille, copyright and registered symbols
    case 'CMU Concrete'
        %for 10 point font and 600 dpi
        n_rows=90;
        glyph_width=76;%will clip some symbols
    case 'Droid Sans Mono'
        %for 10 point font and 600 dpi
        n_rows=95;
        glyph_width=51;
    case 'IBM Plex Mono'
        %for 10 point font and 600 dpi
        n_rows=95;
        glyph_width=51;
    case 'Liberation Mono'
        %for 10 point font and 600 dpi
        n_rows=95;
        glyph_width=51;
    case 'Monoid'
        %for 10 point font and 600 dpi
        n_rows=95;
        glyph_width=51;
    otherwise
        n_rows=1+(idx(idx2(1)+1)-1)-(idx(idx2(1))+1);
        n_rows=floor(n_rows/5)*5;
        ii=find(sum(~glyphs(~m,:))>(n_rows/2));%may clip some symbols
        glyph_width=2*max(abs(ii-size(glyphs,2)/2));
end

c=cell(numel(idx2),1);
k=floor((size(glyphs,2)-glyph_width)/2);
for n=1:numel(idx2)
    rows=idx(idx2(n))+1;
    rows(2)=rows(1)+(n_rows-1);
    rows=rows(1):rows(2);
    c{n}=~glyphs(rows,(k+1):(end-k));
end

N=numel(printable);no_glyph_in_font=false(1,N);
for n=1:N
    no_glyph_in_font(n)=~any(c{n}(:));
end

c(no_glyph_in_font)=[];
printable(no_glyph_in_font)=[];

IM=create_font_png(c,printable);
imwrite(IM,final_image);

%perform cleanup
delete([p_tmp name_base '*']);
if nargout==0,clear,end
end
function IM=create_font_png(c,printable)
%create the png masterfile

%store the 3 dims: glyhp height, glyph width and number of glyphs
sz=[size(c{1}) numel(c)];

%add codepoint in binary as the first column
for n=1:numel(c)
    codepoint=printable(n);
    codepoint=dec2bin(codepoint,sz(1));
    codepoint=reshape(codepoint=='1',sz(1),1);
    c{n}=[codepoint c{n}];
end

%convert to montage (x by 32 chars)
dim=ceil(sz(3)/32)*32;
if numel(c)<dim
    c((numel(c)+1):dim)={false(size(c{1}))};
end
IM=cell2mat(reshape(c,32,[])');

%Now add the 3 dims in the top row. This will only fail if the montage is
%fewer than 60 pixels wide. This is only true for fonts that have extremely
%few glyphs that are extremely narrow.
row1=dec2bin(sz,20)=='1';row1=row1';row1=row1(:)';
row1(size(IM,2))=false;%extend to fit
IM=[row1;IM];
end
